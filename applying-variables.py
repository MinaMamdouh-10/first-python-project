# Variables: Value (string, integer, float, boolean)

# String: A sequence of characters inside quotes
name = 'Mina'
hobby = 'coding'
food = 'apples'
print(f'Hello! My name is {name}. I like {hobby} and eating {food}. ')

# Integer: A whole number
age = 16
num = 4
print(f'I am {age} years old and I have {num} apples. ')

# Float: A number with decimals
price = 1.50
print(f'Each apple costs ${price}. ')

# Boolean: True or False statements
is_graduated = False
goes_to_school = True
print(f'Am I graduated?: {is_graduated}.  Do I go to school?: {goes_to_school}. ')
